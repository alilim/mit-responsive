          In this track we will learn how to design new cameras and create augmented visual experiences. We will focus in detail on how a camera works, how we can tweak existing cameras to capture impossible kinds of photographs and how we can use cameras to enhance human vision. We will address this overflow of imaging devices including mobile cameras, web-cameras, point and shoots and kinects to help design and implement our ideas to solve relevant social and consumer problems.

