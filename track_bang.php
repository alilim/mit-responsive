                 Human computer interfaces have evolved from keyboard, mouse and monitor to interactive surfaces and objects we can grasp and intuitively manipulate. Tangible user interfaces are on the forefront of this development, as they seamlessly couple the dual worlds of bits and atoms by giving physical form to digital information.

          In the course of this track, students will learn the conceptual frameworks of tangible interaction, the latest research in the field, and a variety of techniques for designing and building tangible systems. Throughout the workshop, teams will indulge in a series of short creative assignments, observing everyday interactions of humans with the physical world and each other, and apply these insights to invent new computer interfaces.

          To build and test interface prototypes, students will learn to utilize tools like Arduino, Processing and openFrameworks. These skills in interaction design and prototyping will be applied to a final group project.

