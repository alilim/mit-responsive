          In this track, we focus on the core sensor technologies, sensor conditioning, and interface design to inspire future engineers and designers to consider the importance of not only ""app-driven"" phone applications of sensors, but of a generic sensor-network nervous system. In effect, we argue in this workshop that although the sensor-driven applications of mobile phones are important, future systems will encompass networks and data streams much more complex than your social network.

