    <hr>
    <p style="text-align:justify;margin: 0 0 15px 0;"><img with="200" height="200" src="content/di/pune/faculty/Prof Govind Swarup.jpg" style="float:left;margin: 0px 10px 5px 0px;"/>Prof. G. Swarup was born in Thakurdwara, U.P., in 1929. He obtained
    M.Sc. Degree in Physics from the Allahabad University in 1950, Ph.D.
    degree in the Electrical Engineering Dept. from Stanford University in
    1961 and was awarded D.Sc. by the Roorkee, Benaras and Raipur
    Universities.

    After 2 years in Australia and 7 years in the USA (one year as
    Research Associate at Harvard University and 6 years at Stanford
    including 2 years as a Faculty), he returned to India in 1963 to join
    the Tata Institute of Fundamental Research at Mumbai as Associate
    Professor and retired in 1994 as Professor of Eminence. He was Project
    Director of the GMRT during 1987-1996.

    During the 1960's he built the 530m long and 30m wide radio telescope
    of unique design at Ooty. Later during 1987-1989, he conceived and got
    built the Giant Metrewave Radio Telescope near Narayangaon about 80 km
    north of Pune. GMRT is the world's largest radio telescope working at
    longer wavelengths. It has been designed fully indigenously for
    solving many mysteries of the origin and evolution of the Universe. He
    has published over 115 research papers and edited 4 books. He has two
    patents.

    He is a Fellow of the highly prestigious Royal Society in the U.K.,
    Academician of the International Astronautical Federation, Academician
    of the Pontifical Academy of Sciences and of all the National Science
    Academies in India.

    He has received over 24 awards from India and abroad of which may be
    cited: SS Bhatnagar award 1972, Padma-Shri 1973, Meghnad Saha Medal,
    CV Raman Medal, William Hershel Medal of the Royal Astronomical
    Society 2006, Lifetime Achievement Medal by the Prime Minister of
    India 2008 and other awards by several academies and organizations in
    India, USA, USSR, Italy, Iran and Australia.</p>
