      <h3 style="color:gray;font-family:MuseoSlab;">Prof. Joseph Paradiso</h3>
    </div>
    <hr>
    <p style="text-align:justify;margin: 0 0 15px 0;"><img src="content/di/pune/faculty/joep.jpg" width="200" height="200" class="img-responsive" style="float:left;margin: 0px 10px 5px 0px;"/>Joseph Paradiso is an Associate Professor of Media Arts and Sciences
    at the MIT Media Lab, where he directs the Responsive Environments
    group, which explores how sensor networks augment and mediate human
    experience, interaction, and perception. In addition, he co-directs
    the Things That Think Consortium, a group of industry sponsors and
    Media Lab researchers who explore the extreme fringe of embedded
    computation, communication, and sensing.

    After two years developing precision drift chambers at the Lab for
    High Energy Physics at ETH in Zurich, he joined the Draper Laboratory,
    where his research encompassed spacecraft control systems, image
    processing algorithms, underwater sonar, and precision alignment
    sensors for large high-energy physics detectors. He joined the Media
    Lab in 1994, where his current research interests include embedded
    sensing systems and sensor networks, wearable and body sensor
    networks, energy harvesting and power management for embedded sensors,
    ubiquitous and pervasive computing, localization systems, passive and
    RFID sensor architectures, human-computer interfaces, and interactive
    media. His honors include the 2000 Discover Magazine Award for
    Technological Innovation, and he has authored 200 articles and
    technical reports on topics ranging from computer music to power
    scavenging.

    After receiving a BS in electrical engineering and physics summa cum
    laude from Tufts University, Paradiso became a K.T. Compton fellow at
    the Lab for Nuclear Science at MIT, receiving his PhD in physics there
    for research conducted at CERN in Geneva.</p>
